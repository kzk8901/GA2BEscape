//イニシャライズ
void CObjText::Init()
{
	m_key_flag = false;
	skip_flag = false;
	time = 120;
	word[0] = 0;
	word[1] = 0;
	word[2] = 0;
	word[3] = 0;
	word[4] = 0;

	x = 0;
	y = 0;
}
//アクション
void CObjText::Action()
{
	time += 1;
	if (Input::GetVKey('Z') == true)
		{
			if (m_key_flag == true)
			{
				if (skip_flag == false)
				{
					if (time > 120)
					{
				        if (text_m == 0 && word[0] <= 20 || text_m == 3 && word[0] <= 27 || text_m == 4)
						{
							word[0] += 1;
							m_key_flag = false;
							time = 0;
							skip_flag = true;
						}
						else if (text_m == 1 && word[1] <= 12)
						{
							word[1] += 1;
							m_key_flag = false;
							time = 0;
							skip_flag = true;
						}
						else if (text_m == 2 && word[2] <= 8)
						{
							word[2] += 1;
							m_key_flag = false;
							time = 0;
							skip_flag = true;
						}
						else if (text_m == -1&&word[3]<=52)
						{
							word[3] += 1;
							m_key_flag = false;
							time = 0;
							skip_flag = true;

						}
					}
				}
				else if(time<100)
				{
					time = 100;
				}
				else if(time>120)
				{
					skip_flag = false;
				}


			}
		}
	else
	{
		m_key_flag = true;
	}
}
